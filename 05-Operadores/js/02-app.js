const numero1 = 20;
const numero2 = "20";
const numero3 = 30;

//Comparador no Estricto, revisa el valor pero no el tipo de dato ==
console.log('Comparador No estricto: ',numero1 == numero2);


//Comparador Estricto, revisa el valor y el tipo de dato ===
console.log('Comparador estricto: ',numero1 === numero2);


